import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, algorithmContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    let systemPrompt = `You are an elite AI Code Tutor. Your answers must be extremely organized, spacious, and visually structured.
- Use clear Markdown headings (###, ####) to break up topics.
- Use bullet points and spacing between paragraphs to make reading easy.
- Give highly detailed, step-by-step explanations, and use real code examples.
- For code snippets, ALWAYS use proper Markdown fences specifying the language (e.g., \`\`\`python).
- Your tone should be encouraging, pedagogical, and highly analytical. Provide the 'why' and 'how'.`;

    if (algorithmContext) {
      systemPrompt += `\n\nThe student is currently visualizing the "${algorithmContext.name}" algorithm.
Category: ${algorithmContext.category}
Description: ${algorithmContext.description}
Time Complexity - Best: ${algorithmContext.timeComplexity?.best}, Average: ${algorithmContext.timeComplexity?.average}, Worst: ${algorithmContext.timeComplexity?.worst}
Space Complexity: ${algorithmContext.spaceComplexity}

Focus your answers on this algorithm when relevant, but also answer general CS questions.`;
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded, please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Usage limit reached. Please add credits." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("ai-tutor error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
